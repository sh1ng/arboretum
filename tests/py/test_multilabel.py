import arboretum
import numpy as np
from sklearn.datasets import load_iris
import json
import pytest


def run_regression(depth, true_values, trees=1):
    iris = load_iris()
    y = iris.target
    data = arboretum.DMatrix(iris.data, labels=y)

    config = json.dumps({'objective': 3,
                         'method': 0,
                         'tree':
                         {
                             'labels_count': 3,
                             'eta': 0.2,
                             'max_depth': depth,
                             'gamma': 0.0,
                             'min_child_weight': 2.0,
                             'min_leaf_size': 0,
                             'colsample_bytree': 1.0,
                             'colsample_bylevel': 1.0,
                             'lambda': 0.0,
                             'alpha': 0.0,
                             'initial_y': 0.0
                         }})

    model = arboretum.Garden(config, data)

    for i in range(trees):
        model.grow_tree()
    pred = model.predict(data)

    assert np.allclose(pred, true_values)


